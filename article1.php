             <h3>آشنایی با ابزار Assertible</h3>
             <p>این ابزار به شما اجازه می دهد تا تست های ساده را روی API هایی که طراحی کرده اید تنظیم کرده و API هایی که تولید می کنید را نظارت کنید. شما می توانید API ها را از طریق یک فرمت شخص ثالث رایج که شامل Swagger، Postman و curl اضافه کنید. اگر قصد دارید خارج از دامنه پیش فرض خود آزمایش ها را انجام دهید می توانید این آزمایش ها را بر اساس معیارهای مختلف گروه بندی کرده و آنها را با پارامترهای محیطی مختلف اجرا کنید.

                نکته دیگری که درباره این ابزار وجود دارد این است که شما می توانید تست های Assertible را بر اساس برنامه زمانی مشخص یا از طریق محرک های مختلف اجرا کنید و وضعیت آزمایش ها و تست های خود را روی گیت هاب منتشر کنید.</p>
