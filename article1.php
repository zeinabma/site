            <h3>آشنایی با ابزار Amazon API Gateway</h3>
            <p>AWS Free Tier امروزه به توسعه دهندگان دسترسی بدون هزینه به اکثر سرویس های AWS را ارائه می دهد که این سرویس ها شامل سرویس Amazon API Gateway نیز می باشد. API رایگان آمازون شما حداقل یک میلیون فراخوانی به API را برای شما فراهم می کند و باید بدانید که تنها یک سال طول عمر دارد ولی با این حال برای شروع طراحی API و توسعه و تست آن می تواند گزینه بسیار مناسبی باشد. نسخه کامل و با هزینه Amazon API Gateway به شما اجازه می دهد تا بتوانید API های فرانت اند را نیز برای خود طراحی کنید که البته این کار تنها برای اپلیکیشن هایی که روی Amazon EC2، AWS Lambda یا هر اپلیکیشن تحت وب دیگری که با استفاده از ابزارهای متا مانند traffic management، API version control و ابزارهای نظارتی روی تمام پکیج ها ساخته شده اند امکان پذیر خواهد بود.</p>
         
            <h3>APImetrics از ابزارهای طراحی و توسعه API و توسعه و تست آن</h3>
            <p>APImetrics در واقع یک سرویس نظارت و هشدار مخصوص API است که شامل یک طراح بصری API بوده و از هر دو نوع API REST و SOAP پشتیبانی می کند و البته حرکت بین این دو نوع API را نیز برای شما ساده تر می کند. این ابزار که می تواند به میزان زیادی در طراحی API کمک کننده باشد یک سیستم گردش کار را برای شما فراهم می کند که به شما کمک می کند تا چندین فراخوانی از API خود را به ترتیب ایجاد کنید و به این ترتیب می توانید هر چه که باید درست پیش برود ولی دچار مشکل می شود را مدیریت کنید. دقت داشته باشید که هیچ نسخه رایگانی از این ابزار وجود ندارد ولی شرکت سازنده آن یک طرح آزمایشی رایگان 14 روزه از سطوح مختلف خدمات خود را به شما ارائه می دهد. با استفاده از این طرح شما می توانید تا حداقل 20 هزار فراخوانی در ماه داشته باشید. شما تنها با پرداخت 18 دلار در ماه می توانید از طیف گسترده ای از خدمات این ابزار نیز استفاده کنید.</p>

